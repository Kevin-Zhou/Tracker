public class Library {
	private int numBooks; // Number of books in the library collection
	private int numStudents; // Number of users

	public Library(int numBooks, int numStudents) {
		this.numBooks = numBooks;
		this.numStudents = numStudents;
	}

	// Getters and Setters
	public int getStudents() {
		return numStudents;
	}
	public int getBooks() {
		return numBooks;
	}
	
	
}
